package com.chinaops.web.edesktop.entity;

/**
 * Description: 组管理
 */
public class GroupStrategy {
	private int id;
	private String groupId;
	private String strategiesId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public String getStrategiesId() {
		return strategiesId;
	}
	public void setStrategiesId(String strategiesId) {
		this.strategiesId = strategiesId;
	}


}
